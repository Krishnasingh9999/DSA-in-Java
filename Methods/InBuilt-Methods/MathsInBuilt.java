public class MathsInBuilt {
public static void main(String[] args) {
  System.out.println(Math.sqrt(4));
  System.out.println(Math.cbrt(27));
  System.out.println(Math.abs(-64));
  System.out.println(Math.floor(3.7)); //Also called greatest integer function (GIF) or floor 
  System.out.println(Math.floor(-6.8));
  System.out.println(Math.ceil(3.00001));
  System.out.println(Math.floorDiv(4,2));
  System.out.println(Math.min(20.1, 100));
  System.out.println(Math.min(3.5, -9.6));
  System.out.println(Math.max(75, 100));

}
  
}